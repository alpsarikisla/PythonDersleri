# KarsilastirmaOperatorleri
# karşılaştırma operatörleri boolean(mantıksal) türünde veri döndürürler
sayi1 = 7
sayi2 = 3
print("Sayı 1, Sayı 2 den büyük mü?")
print(sayi1 > sayi2)

print("Sayı 1, sayi2 den küçük mü?")
print(sayi1 < sayi2)

print("Büyük eşit mi?")
print(sayi1 >= sayi2)

print("Küçük eşit mi?")
print(sayi1 <= sayi2)

print("Eşit mi ? ")
print(sayi1 == sayi2)

print("Eşit Değil mi?")
print(sayi1 != sayi2)
