# AritmetikselOperatorler
# + , - , * , / Aritmetiksel operitörlerimizdir

sayi1 = 13
sayi2 = 5

toplam = sayi1 + sayi2
print("Sayıları toplamı = ")
print(toplam)

fark = sayi1 - sayi2
print("Sayıların Farkı = ")
print(fark)

carpim = sayi1 * sayi2
print("Sayıların Çarpımı = ")
print(carpim)

bolum = sayi1 / sayi2
print("Sayıların Bölümü = ")
print(bolum)

mod = sayi1 % sayi2
print("Sayıların Bölümünden Kalan = ")
print(mod)

